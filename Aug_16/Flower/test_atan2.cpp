#include <bits/stdc++.h>
using namespace std;

int main()
{
	cout<<atan2(1,0)<<endl;
	cout<<atan2(1,1)<<endl;
	cout<<atan2(0,1)<<endl;
	cout<<atan2(-1,1)<<endl;
	cout<<atan2(-1,0)<<endl;
	cout<<atan2(-1,-1)<<endl;
	cout<<atan2(0,-1)<<endl;
	cout<<atan2(1,-1)<<endl;
	cout<<atan2(1,0)<<endl;
}